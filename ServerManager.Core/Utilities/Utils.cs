using System;
using System.IO;

using Shared;

namespace ServiceManager.Core
{
    public static class Utils
    {
        public static string GetPath(PathType pathType)
        {
            string Result = Utilities.CurrentPath(true);

            if (pathType == PathType.Root)
                    return (Result);

            Result = $"{Result}{pathType.ToString()}\\";

            if (!Directory.Exists(Result))
                Directory.CreateDirectory(Result);

            return (Result);
        }
    }
}
